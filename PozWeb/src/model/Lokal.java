package model;

public class Lokal {

	private String name, address, pib, mb, description;
	private double latitude, longitude;
	
	public Lokal(String name, String address, String pib, String mb, String description, double latitude,
			double longitude) {
		super();
		this.name = name;
		this.address = address;
		this.pib = pib;
		this.mb = mb;
		this.description = description;
		this.latitude = latitude;
		this.longitude = longitude;
	}
	
	
	
}
